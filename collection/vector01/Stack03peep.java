package collection.vector01;

import java.util.Stack;

public class Stack03peep {

	public static void main(String[] args) {
		Stack s=new Stack();
		s.push(101);
		s.push(102);
		s.push(103);
		s.push(104);
		System.out.println(s);
		System.out.println(s.peek());
		System.out.println(s);
		
	}
}
